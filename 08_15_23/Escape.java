public class Escape {
    public static void main (String[] args){
        System.out.println("Hello my name is \t Noah");
        System.out.println("Hello my name is \b Noah");
        System.out.println("Hello my name is \n Noah");
        System.out.println("Hello my name is \r Noah");
        System.out.println("Hello my name is \f Noah");
        System.out.println("Hello my name is \' Noah");
        System.out.println("Hello my name is \" Noah");
        System.out.println("Hello my name is \\ Noah");
    }
    
}
